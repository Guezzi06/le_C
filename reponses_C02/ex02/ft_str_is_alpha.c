int	ft_str_is_alpha(char *str)
{
	int		i;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z') == 0)
		{
			if ((str[i] >= 'A' && str[i] <= 'Z') == 0)
				return (0);
		}
		i++;
	}
	return (1);
}
