#include <unistd.h>

void	ft_continue(char b[]);

void	ft_print_comb(void)
{
	char	b[3];
	char	e[2];
	int		i;

	b[0] = '0';
	b[1] = '0';
	b[2] = '0';
	e[0] = ',';
	e[1] = ' ';
	i = 0;
	while (i < 999)
	{
		if (b[0] < b[1] && b[1] < b[2])
		{
			write(1, b, 3);
			if ((b[0] == '7' && b[1] == '8' && b[2] == '9') == 0)
			{
				write(1, e, 2);
			}
		}
		ft_continue(b);
		i++;
	}
}

void	ft_continue(char b[])
{
	b[2]++;
	if (b[2] == ':')
	{
		b[2] = '0';
		b[1]++;
	}
	if (b[1] == ':')
	{
		b[1] = '0';
		b[0]++;
	}
}
