int	ft_is_sort(int *tab, int length, int (*f)(int, int))
{
	int	a;
	int	b;
	int	i;

	a = 0;
	b = 0;
	i = 0;
	while (i < length - 1)
	{
		if ((*f)(tab[i], tab[i + 1]) > 0)
			a++;
		if ((*f)(tab[i], tab[i + 1]) < 0)
			b++;
		if (a != 0 && b != 0)
			return (0);
		i++;
	}
	return (1);
}
